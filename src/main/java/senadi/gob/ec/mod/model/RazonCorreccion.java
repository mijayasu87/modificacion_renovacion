package senadi.gob.ec.mod.model;

/**
 *
 * @author micharesp
 */
public class RazonCorreccion {

    private String tipoModificacion;
    private String tramites;
    private String razon;
    private String suscriptor;
    private String denominacion;

    /**
     * @return the tipoModificacion
     */
    public String getTipoModificacion() {
        return tipoModificacion;
    }

    /**
     * @param tipoModificacion the tipoModificacion to set
     */
    public void setTipoModificacion(String tipoModificacion) {
        this.tipoModificacion = tipoModificacion;
    }

    /**
     * @return the tramites
     */
    public String getTramites() {
        return tramites;
    }

    /**
     * @param tramites the tramites to set
     */
    public void setTramites(String tramites) {
        this.tramites = tramites;
    }

    /**
     * @return the razon
     */
    public String getRazon() {
        return razon;
    }

    /**
     * @param razon the razon to set
     */
    public void setRazon(String razon) {
        this.razon = razon;
    }

    /**
     * @return the suscriptor
     */
    public String getSuscriptor() {
        return suscriptor;
    }

    /**
     * @param suscriptor the suscriptor to set
     */
    public void setSuscriptor(String suscriptor) {
        this.suscriptor = suscriptor;
    }

    /**
     * @return the denominacion
     */
    public String getDenominacion() {
        return denominacion;
    }

    /**
     * @param denominacion the denominacion to set
     */
    public void setDenominacion(String denominacion) {
        this.denominacion = denominacion;
    }
}
